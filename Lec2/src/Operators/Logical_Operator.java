package Operators;

public class Logical_Operator {
	public static void main(String[] args) {
		boolean first = true;
		boolean second = false;
		
		// &&
		System.out.println(first && second);
		
		// ||
		System.out.println(first || second);
		
		// !
		System.out.println(first && !second);
	}
}
