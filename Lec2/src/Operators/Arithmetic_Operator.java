package Operators;

public class Arithmetic_Operator {
	public static void main(String[] args) {
		int a =5;
		int b=10;
		int ans=0;
		//add
		ans=a+b;
		System.out.println("Addition: "+ ans);
		
		//Subtraction
		ans=a-b;
		System.out.println("Subtract: "+ ans);
		
		// Multiply
		ans = a * b;
		System.out.println("Multiply: " + ans);

		// Division
		ans = a / b;
		System.out.println("Division: " + ans);
		
		// Modulus
		ans = a % b;
		System.out.println("Division: " + ans);
	}
}
